                    <h2>
                        Light-Emitting-Diodes (LED) Alternatives
                    </h2>
                    <h4>
                        Light-Emitting-Diodes (LED) Signs or Neon? if that’s a question we can help you with the answer.
                    </h4>
                    <ul>
                        <li>
                            LEDs use 6-10 times less power than neon, or 10 watts vs Neon’s 96 watts
                        </li>
                        <li>
                            Run cooler – Neon electrodes, that are attached to each tube, do get hot and occasionally burn out wires or transformers. LEDs only reach 140°-212°F and have no transformers to blow out.
                        </li>
                        <li>
                            25-35% thinner - Without neon tubes LED signs can be as thin as 1 inch while Neon signs’ thickness runs around 5 inches thick.
                        </li>
                        <li>
                            Last longer - neon lasts 7-10 years and fade over time, while LED’s last longer than 10 years
                        </li>
                        <li>
                            Burn brighter - LEDs produce 100 lumens per watt, neon only 50
                        </li>
                        <li>
                            Flexible so you have more animation options, which can sometimes cause permitting issues if your signs flash.
                        </li>
                        <li>
                            Can be reusable – The flexibility of LEDs can allow you to recycle the elements into another sign. A very important element for the environmentally conscience. Once you purchase a neon sign that’s it. The sign can never be changed or updated like an LED.
                        </li>
                        <li>
                            Not as affected by the ambient temperature – LEDs are affected by excessive heat but not so much the cold. While neon and argon gasses get lazy in the cold and don’t appear as crisp and bright.
                        </li>
                    </ul>
